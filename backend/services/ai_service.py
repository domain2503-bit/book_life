async def _determine_book_category(book_title: str, author: str, description: str = "") -> str:
    """BOOK_DB → 설명 키워드 → Gemini AI 순서로 책 카테고리 판별"""
    cached = _lookup_book_category(book_title)
    if cached:
        return cached

    from services.naver_service import _classify_from_text
    keyword_result = _classify_from_text(book_title, description)
    if keyword_result:
        return keyword_result

    if _is_mock():
        return "자기계발"

    client = _get_client()
    prompt = CLASSIFY_PROMPT.format(title=book_title, author=author)
    response = await _call_gemini(client, "gemini-2.5-flash-lite", prompt)
    result = response.text.strip()
    if result in VALID_CATEGORIES:
        return result
    for cat in VALID_CATEGORIES:
        if cat in result:
            return cat
    return "자기계발"


async def generate_action_items(
    book_title: str, author: str, reviews: list[str],
    book_category: str | None = None, description: str = ""
) -> list[dict]:
    # 카테고리 확정 (전달받거나 자동 판별)
    if not book_category or book_category not in VALID_CATEGORIES:
        book_category = await _determine_book_category(book_title, author, description)

    if _is_mock():
        return _mock_action_items(book_title, book_category)

    client = _get_client()
    reviews_text = "\n\n".join(reviews) if reviews else "서평 데이터가 없습니다."
    prompt = ACTION_PROMPT_TEMPLATE.format(
        title=book_title,
        author=author,
        category=book_category,
        reviews=reviews_text[:8000],
    )
    response = await _call_gemini(
        client, "gemini-2.5-flash-lite", prompt,
        config=types.GenerateContentConfig(
            system_instruction="당신은 독서 인사이트 추출 전문가입니다. 반드시 JSON 배열만 반환하세요."
        ),
    )
    raw = response.text.strip()
    items = json.loads(_sanitize_json(_extract_json(raw)))

    # 모든 아이템에 동일 카테고리 강제 적용
    for item in items:
        item["category"] = book_category

    return items


